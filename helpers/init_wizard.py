# helpers/init_wizard.py
"""
Interactive first-run setup wizard (python main.py --init).

Guides the user through:
  Step 1 — Data provider + optional Polygon API key
  Step 2 — Capital & backtest dates
  Step 3 — What to test (single symbol / portfolio)
  Step 4 — Write config_starter.py (and optionally .env)

Design constraints
------------------
- stdlib only: sys, os, pathlib, textwrap, datetime — no pip installs.
- No network calls.
- Nothing written to disk without explicit user confirmation.
- Will not overwrite an existing config.py.
"""

from __future__ import annotations

import os
import re
import sys
import textwrap
from datetime import datetime
from pathlib import Path

# ---------------------------------------------------------------------------
# Colour / formatting helpers
# ---------------------------------------------------------------------------

# Disable ANSI on Windows cmd / non-TTY environments.
_USE_COLOR: bool = sys.stdout.isatty() and os.name != "nt"


def _ansi(code: str, text: str) -> str:
    return f"\033[{code}m{text}\033[0m" if _USE_COLOR else text


def bold(text: str) -> str:
    return _ansi("1", text)


def green(text: str) -> str:
    return _ansi("32", text)


def yellow(text: str) -> str:
    return _ansi("33", text)


def cyan(text: str) -> str:
    return _ansi("36", text)


def red(text: str) -> str:
    return _ansi("31", text)


# ---------------------------------------------------------------------------
# Low-level prompt helpers
# ---------------------------------------------------------------------------

def _section(title: str) -> None:
    """Print a bold section separator."""
    width = 60
    print()
    print(bold("=" * width))
    print(bold(f"  {title}"))
    print(bold("=" * width))


def _ask(prompt: str, choices: list[str] | None = None, default: str = "") -> str:
    """
    Prompt the user for input, validate against *choices* if supplied.

    Returns the lowercase-stripped answer (or *default* on empty Enter).
    Exits cleanly on Ctrl-C / EOF.
    """
    choice_hint = f"[{'/'.join(choices)}]" if choices else ""
    default_hint = f" (default: {default})" if default else ""
    full_prompt = f"  {prompt} {choice_hint}{default_hint}: "

    while True:
        try:
            raw = input(full_prompt).strip().lower()
        except (EOFError, KeyboardInterrupt):
            print()
            sys.exit(0)

        answer = raw or default.lower()

        if choices and answer not in [c.lower() for c in choices]:
            print(red(f"  Please enter one of: {', '.join(choices)}"))
            continue

        return answer


def _confirm(prompt: str) -> bool:
    """Prompt for y/n. Returns True for 'y'."""
    return _ask(prompt, choices=["y", "n"], default="y") == "y"


