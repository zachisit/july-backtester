def run_init_wizard() -> None:
    """Interactive wizard — called when `python main.py --init` is invoked."""
    project_root = Path(__file__).resolve().parent.parent

    print()
    print(bold(cyan("  July Backtester — First-Time Setup Wizard")))
    print(cyan("  Press Ctrl-C at any time to exit without writing files."))

    # ------------------------------------------------------------------ #
    # Step 1 — Data provider                                              #
    # ------------------------------------------------------------------ #
    _section("Step 1 of 4 — Data Provider")
    print(textwrap.dedent("""\
      Available providers:
        yahoo    — free, no API key needed (default)
        csv      — load OHLCV CSV files from a local folder
        polygon  — Polygon.io REST API (free tier available)
        norgate  — Norgate Data subscription required
    """))

    provider = _ask("Which provider?", choices=["yahoo", "csv", "polygon", "norgate"], default="yahoo")

    polygon_api_key = ""
    if provider == "polygon":
        print()
        print("  Your Polygon.io API key will be written to .env (POLYGON_API_KEY=...).")
        print("  Press Enter to skip (you can add it manually later).")
        try:
            polygon_api_key = input("  Polygon API key: ").strip()
        except (EOFError, KeyboardInterrupt):
            print()
            sys.exit(0)

    # ------------------------------------------------------------------ #
    # Step 2 — Capital & dates                                            #
    # ------------------------------------------------------------------ #
    _section("Step 2 of 4 — Capital & Dates")

    # initial_capital
    while True:
        raw_capital = _ask("Initial capital (USD)", default="100000")
        try:
            capital = float(raw_capital.replace(",", ""))
            if capital <= 0:
                raise ValueError
            break
        except ValueError:
            print(red("  Must be a positive number (e.g. 100000)."))

    # start_date
    while True:
        start_date = _ask("Start date (YYYY-MM-DD)", default="2010-01-01")
        try:
            datetime.strptime(start_date, "%Y-%m-%d")
            break
        except ValueError:
            print(red("  Invalid date. Use YYYY-MM-DD format."))

    # end_date — always use dynamic today expression in the written file
    end_expr = 'datetime.now().strftime("%Y-%m-%d")'
    print(f"\n  {yellow('End date')}: today (dynamic — always runs to the latest available bar)")

    # ------------------------------------------------------------------ #
    # Step 3 — What to test                                               #
    # ------------------------------------------------------------------ #
    _section("Step 3 of 4 — What to Test")
    print(textwrap.dedent("""\
      single    — one or more individual symbols (e.g. AAPL, MSFT)
      portfolio — a named basket (Nasdaq 100 JSON or custom list)
    """))

    mode = _ask("Mode?", choices=["single", "portfolio"], default="single")

    if mode == "single":
        print()
        print("  Enter one or more ticker symbols separated by commas.")
        raw_tickers = _ask("Symbols", default="SPY")
        symbols: list[str] = [t.strip().upper() for t in raw_tickers.split(",") if t.strip()]
        symbols_or_portfolio: object = symbols
    else:
        print()
        print(textwrap.dedent("""\
          Portfolio options:
            nasdaq100  — uses the bundled nasdaq_100.json file
            custom     — enter a comma-separated list of tickers
        """))
        port_choice = _ask("Portfolio type?", choices=["nasdaq100", "custom"], default="nasdaq100")

        if port_choice == "nasdaq100":
            symbols_or_portfolio = {"Nasdaq 100": "nasdaq_100.json"}
        else:
            raw_tickers = _ask("Tickers (comma-separated)", default="AAPL,MSFT,GOOGL")
            tickers: list[str] = [t.strip().upper() for t in raw_tickers.split(",") if t.strip()]
            portfolio_name = _ask("Portfolio name", default="My Portfolio")
            symbols_or_portfolio = {portfolio_name: tickers}

    # ------------------------------------------------------------------ #
    # Step 4 — Confirm & write                                            #
    # ------------------------------------------------------------------ #
    _section("Step 4 of 4 — Review & Write")

    config_path = project_root / "config.py"
    config_exists = config_path.exists()

    print(f"\n  {bold('Will write:')}")
    print(f"    {green(str(config_path))}", end="")
    if config_exists:
        print(yellow("  (existing file — wizard values will be patched in-place)"))
    else:
        print()
    if polygon_api_key:
        print(f"    {green(str(project_root / '.env'))}  (POLYGON_API_KEY)")

    print()
    if not _confirm("Proceed and write files?"):
        print(yellow("  Aborted. No files written."))
        return

    # Write or patch config.py
    if config_exists:
        patched_text, changes = _patch_existing_config(
            config_path, provider, capital, start_date, mode, symbols_or_portfolio
        )
        config_path.write_text(patched_text, encoding="utf-8")
        print(green(f"\n  Updated: {config_path}"))
        for ch in changes:
            print(f"    {cyan('→')} {ch}")
        if not changes:
            print(yellow("  (no keys matched — no changes made)"))
    else:
        config_text = _build_config(provider, capital, start_date, end_expr, mode, symbols_or_portfolio)
        config_path.write_text(config_text, encoding="utf-8")
        print(green(f"\n  Written: {config_path}"))

    # Write .env if polygon key supplied
    if polygon_api_key:
        env_path = project_root / ".env"
        marker = "POLYGON_API_KEY="
        existing = env_path.read_text(encoding="utf-8") if env_path.exists() else ""
        if marker not in existing:
            with env_path.open("a", encoding="utf-8") as fh:
                if existing and not existing.endswith("\n"):
                    fh.write("\n")
                fh.write(f"POLYGON_API_KEY={polygon_api_key}\n")
            print(green(f"  Written: {env_path}  (POLYGON_API_KEY appended)"))
        else:
            print(yellow(f"  Skipped: {env_path}  (POLYGON_API_KEY already present)"))

    # Next steps
    print()
    print(bold("  Next steps:"))
    if config_exists:
        print(f"  1. Review {cyan('config.py')} — all other settings are unchanged.")
    else:
        print(f"  1. Review {cyan('config.py')} and adjust any other settings you need.")
    print(f"  2. Run:  {cyan('python main.py')}")
    print()
